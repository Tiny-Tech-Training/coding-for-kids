# Variables and Data Types
apples = 4  # An integer variable
juice_glasses = 2.5  # A float variable

# Arithmetic Operations
total_apples = apples + 2  # Adding 2 to the apples variable
half_juice = juice_glasses / 2  # Dividing juice_glasses by 2
combined_amount = apples + juice_glasses  # Adding apples and juice_glasses together

# Printing the results
print("Total apples:", total_apples)
print("Half juice:", half_juice)
print("Combined amount:", combined_amount)